<p><b><u> Principe du jeu </u></b></p>

<p>

Le jeu se compose de paires de cartes portant des illustrations identiques. 

L'ensemble des cartes est mélangé, puis étalé face contre table. 

À son tour, chaque joueur retourne deux cartes de son choix. 

S'il découvre deux cartes identiques, il les ramasse et les conserve, ce qui lui permet de rejouer. Si les cartes ne sont pas identiques, il les retourne faces cachées à leur emplacement de départ.

Le jeu se termine quand toutes les paires de cartes ont été découvertes et ramassées. 

Le gagnant est le joueur qui possède le plus de paires.

</p>

